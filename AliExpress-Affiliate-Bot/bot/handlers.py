async def start_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Handle the /start command with a Hebrew welcome message.

    Also clears any in-progress clarification state so /start always resets.

    Args:
        update: Incoming Telegram update.
        context: Callback context from the dispatcher.
    """
    context.user_data.pop(_CLARIFY_KEY, None)
    user = update.effective_user
    logger.info("User %s started the bot.", user.id if user else "unknown")
    await update.message.reply_text(WELCOME)


async def search_handler(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """Handle a free-text message — either a clarification answer or a new search query.

    If a clarification loop is active for this user, routes to
    _handle_clarification_answer. Otherwise starts the full pipeline,
    optionally triggering the clarification loop first for broad queries.

    Args:
        update: Incoming Telegram update.
        context: Callback context from the dispatcher.
    """
    user_text: str = (update.message.text or "").strip()
    user_id:   int = update.effective_user.id

    if not user_text:
        return

    # Route to clarification answer handler if loop is active.
    if context.user_data.get(_CLARIFY_KEY):
        await _handle_clarification_answer(update, context)
        return

    logger.info("New search from user %s: %r", user_id, user_text)

    # Check if the query needs clarification.
    try:
        clarification = await should_clarify_query(user_text)
    except LLMError as exc:
        logger.warning(
            "Clarification check failed for user %s (%s) — skipping to search.", user_id, exc
        )
        clarification = {"needs_clarification": False, "questions": []}

    if clarification.get("needs_clarification") and clarification.get("questions"):
        context.user_data[_CLARIFY_KEY] = {
            "original_query": user_text,
            "questions":      clarification["questions"],
            "current_idx":    0,
            "answers":        [],
        }
        await _ask_clarification_question(update, context)
        return

    # No clarification needed — run the pipeline directly.
    holding: Message = await update.message.reply_text(SEARCHING_TRANSLATING)
    try:
        translation = await translate_to_english(user_text)
    except TranslationError as exc:
        logger.error("Translation failed for user %s: %s", user_id, exc)
        await holding.edit_text(ERROR_LLM_FAILURE)
        return

    logger.info(
        "Translated %r → strategy=%s brands=%d category=%r window=₪%d–₪%d hints=%d",
        user_text,
        translation.search_strategy,
        len(translation.search_plan.brand_queries),
        translation.search_plan.category_query,
        translation.search_plan.price_window_ils[0],
        translation.search_plan.price_window_ils[1],
        len(translation.search_plan.confidence_hints),
    )
    await _run_search_pipeline(update, context, user_text, translation, holding)


async def skip_callback_handler(
    update: Update, context: ContextTypes.DEFAULT_TYPE
) -> None:
    """Handle the 'skip clarification' inline button press.

    Clears the clarification state and immediately runs the search pipeline
    using the original query (with plain translation), so the user is never stuck.

    Args:
        update: Incoming Telegram update containing the callback query.
        context: Callback context from the dispatcher.
    """
    query = update.callback_query
    await query.answer()

    state:          dict = context.user_data.pop(_CLARIFY_KEY, {})
    original_query: str  = state.get("original_query", "")
    user_id:        int  = update.effective_user.id

    logger.info("User %s skipped clarification for query %r.", user_id, original_query)

    holding: Message = query.message
    await holding.edit_text(CLARIFICATION_SKIPPED, reply_markup=None)

    if not original_query:
        return

    await holding.edit_text(SEARCHING_TRANSLATING)
    try:
        translation = await translate_to_english(original_query)
    except TranslationError as exc:
        logger.error("Translation failed for user %s after skip: %s", user_id, exc)
        await holding.edit_text(ERROR_LLM_FAILURE)
        return

    await _run_search_pipeline(update, context, original_query, translation, holding)
